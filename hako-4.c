/********************************************************************************/
/*		hako_play																*/
/********************************************************************************/
void hako_play()
{
	int sw, savex, savey, x, y, x1, y1, nextx, nexty, koma, koma_numb, cnt;
	
	lcd_clear(WHITE);
	memcpy(hako_ban, hako_ban_dat, sizeof(hako_ban));
	hako_disp_ban();					// 表示
	for(;;){
		/***	駒を選択する	***/
		for(;;){
			sw = sw_updown();
			if(sw & 2){						// sw2: exit
				return;
			}
			if(sw & 4){						// touch
				savex = tch_x;
				savey = tch_y;
				x = (savex-80)/40+1;
				y = (savey-20)/40+1;
				if(x<1 || x>4 || y<1 || y>5 || hako_ban[y][x]==0)
					continue;				// 駒でない
				break;
			}
		}
		/***	駒を移動する	***/
		koma = (unsigned char)hako_ban[y][x];
		koma_numb = koma % 10;
//		tooshibango = koma / 10;
		while(koma==(unsigned char)hako_ban[y][x-1]){	// x,y を左上にする
			x--;
		}
		while(koma==(unsigned char)hako_ban[y-1][x]){
			y--;
		}
		
		cnt = 0;
		for(;;){
			if(tch_read(&x1, &y1)==0){		// no touch
				wait_1ms();
				if(--cnt < -200)
					break;					// 再度駒の選択に戻る
			}
			else{							// touch
				/***	移動したかテスト	***/
				cnt = 0;
				nextx = x;
				nexty = y;
				if(y1-savey > 40)
					nexty = y+1;
				else if(y1-savey < -40)
					nexty = y-1;
				else if(x1-savex > 40)
					nextx = x+1;
				else if(x1-savex < -40)
					nextx = x-1;
                else
                    continue;
				/***	移動できるかテスト	***/
				hako_remove_koma(hako_ban, koma);
				if(hako_put_koma_test(hako_ban, nextx, nexty, koma_numb)){	// 移動できる
					x = nextx;
					y = nexty;
					savex = x1;
					savey = y1;
				}
				hako_put_koma(hako_ban, x, y, koma);	// 駒を置く
				hako_disp_ban();
			}
		}
	}
}
/********************************************************************************/
/*		hako_put_koma_test														*/
/*		return 1: 置ける、0:置けない											*/
/********************************************************************************/
int hako_put_koma_test(char ban[7][6], int x, int y, int koma_numb)
{
	int x1, y1;
	
	for(y1=0; y1<2; y1++){
		for(x1=0; x1<2; x1++){
			if(ban[y+y1][x+x1] && hako_koma_pat[koma_numb][y1][x1])
				return 0;				// 置けない
		}
	}
	return 1;							// 置ける
}
/********************************************************************************/
/*		hako_put_koma															*/
/********************************************************************************/
void hako_put_koma(char ban[7][6], int x, int y, int koma)
{
	int koma_numb = koma % 10;
	int x1, y1;
	
	for(y1=0; y1<2; y1++){
		for(x1=0; x1<2; x1++){
			if(hako_koma_pat[koma_numb][y1][x1])
				ban[y+y1][x+x1] = koma;	// put koma
		}
	}
}
/********************************************************************************/
/*		hako_remove_koma														*/
/********************************************************************************/
void hako_remove_koma(char ban[7][6], int koma)
{
	int x, y;
	
	for(y=0; y<7; y++){
		for(x=0; x<6; x++){
			if(ban[y][x]==koma)
				ban[y][x] = 0;			// remove
		}
	}
}
/********************************************************************************/
/*		hako_disp_ban															*/
/********************************************************************************/
void hako_disp_ban()
{
	int i, x, y, k, p[5], pcnt, pos, u, d, r, l, color;
	
	for(k=0; k<=10; k++){					// 駒ループ
		memset(p, 0, sizeof(p));
		pcnt = 0;
		for(pos=0; pos<7*6; pos++){
			if(k==hako_banp[pos]/10){
				p[pcnt++] = pos;
				if(pcnt>=5)
					message("hako_disp_ban err k=%d\n", k);
			}
		}
		color = k==0 ? WHITE : hako_color[k];
		for(i=0; i<pcnt; i++){
			x = macrox(p[i]);
			y = macroy(p[i]);
			lcd_clearA(80+(x-1)*40, 20+(y-1)*40, 80+x*40-1, 20+y*40-1, color);
			if(k){
				u = d = r = l = 1;
				if(hako_banp[p[i]+U]/10==k)		// 同じ駒が上に続く
					u = 0;
				if(hako_banp[p[i]+D]/10==k)
					d = 0;
				if(hako_banp[p[i]+R]/10==k)
					r = 0;
				if(hako_banp[p[i]+L]/10==k)
					l = 0;
				if(u)
					lcd_clearA(80+(x-1)*40, 20+(y-1)*40, 80+x*40-1, 20+(y-1)*40, BLACK);
				if(d)
					lcd_clearA(80+(x-1)*40, 20+y*40-1, 80+x*40-1, 20+y*40-1, BLACK);
				if(l)
					lcd_clearA(80+(x-1)*40, 20+(y-1)*40, 80+(x-1)*40, 20+y*40-1, BLACK);
				if(r)
					lcd_clearA(80+x*40-1, 20+(y-1)*40, 80+x*40-1, 20+y*40-1, BLACK);
			}
		}
	}
	lcd_clearA(80-1, 20-1, 80+160, 20-1, BLACK);
	lcd_clearA(80-1, 20+200, 80+160, 20+200, BLACK);
	lcd_clearA(80-1, 20-1, 80-1, 20+200, BLACK);
	lcd_clearA(80+160, 20-1, 80+160, 20+200, BLACK);
}
/********************************************************************************/
/*		hako_main																*/
/********************************************************************************/
void hako_main()
{
	int i, j, sw;
	
	lcd_clear(WHITE);
	srand(TickCount);
	memcpy(hako_ban, hako_ban_dat, sizeof(hako_ban));
	hako_disp_ban();								// 表示
	lcd_disp_str_x1(250,20,"Push");
	lcd_disp_str_x1(250,40,"any");
	lcd_disp_str_x1(250,60,"sw");
	sw_updown();
	lcd_clear(WHITE);
	hako_disp_ban();
	for(i=0; ; i++){
		if(hako_kif[i][0]==0)
			break;
		memcpy(hako_ban, hako_kif[i], 7*6);
		hako_disp_ban();
		for(j=0; j<5; j++){
			sw = sw_read();
//			if(sw)
//				sw_downup();
            if(sw & 4)              // sw3: return
                return;
			wait_ms(100);
		}
	}
	lcd_disp_str_x1(250,20,"End");
	sw_updown();
}




